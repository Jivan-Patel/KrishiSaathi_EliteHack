const translateData = (data, lang) => {
    if (!lang || lang === 'en' || !dataTranslations[lang]) return data;

    const dict = dataTranslations[lang];

    const translateValue = (val, type) => {
        if (!val) return val;
        if (type === 'crop' && dict.crops[val]) return dict.crops[val];
        if (type === 'fert' && dict.fertilizers[val]) return dict.fertilizers[val];
        if (type === 'season' && dict.seasons[val]) return dict.seasons[val];
        if (type === 'soil' && dict.soils[val]) return dict.soils[val];
        return val;
    };

    const processObject = (obj) => {
        if (Array.isArray(obj)) return obj.map(item => processObject(item));
        if (typeof obj !== 'object' || obj === null) return obj;

        const newObj = { ...obj };

        // Crop data processing
        if (newObj.name && !newObj.seller) { // Heuristic for crop object
            newObj.name = translateValue(newObj.name, 'crop');
        }
        if (newObj.season) {
            newObj.season = translateValue(newObj.season, 'season');
        }
        if (newObj.soilTypes) {
            newObj.soilTypes = newObj.soilTypes.map(s => translateValue(s, 'soil'));
        }
        if (newObj.suitableCrops) {
            newObj.suitableCrops = newObj.suitableCrops.map(c => translateValue(c, 'crop'));
        }

        // Fertilizer data processing
        if (newObj.name && newObj.seller) { // Heuristic for fertilizer object
            newObj.name = translateValue(newObj.name, 'fert');
        }

        // Recursive processing for nested objects (like commonPests)
        for (let key in newObj) {
            if (typeof newObj[key] === 'object') {
                newObj[key] = processObject(newObj[key]);
            }
        }

        return newObj;
    };

    return processObject(data);
};

module.exports = { translateData };
